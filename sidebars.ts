import fs from "node:fs";
import path from "node:path";
import type { SidebarsConfig } from "@docusaurus/plugin-content-docs";

const ontologySidebarIds = [
	"entity",
	"concept",
	"operation",
	"specification",
	"troubleshooting",
	"comparison",
] as const;

function hasDocsDir(dirName: string): boolean {
	return fs.existsSync(path.join(process.cwd(), "docs", dirName));
}

const sidebars = Object.fromEntries(
	ontologySidebarIds
		.filter((dirName) => hasDocsDir(dirName))
		.map((dirName) => [dirName, [{ type: "autogenerated", dirName }]]),
) as SidebarsConfig;

export default sidebars;
